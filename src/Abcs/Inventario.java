    private void boton_SacarCantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_SacarCantidadActionPerformed
        // TODO add your handling code here:
        try {
            String productoId = clave_TextField.getText();
            int cantidadIngresada = Integer.parseInt(cantidad_TextField.getText());
        
            // Realizar búsqueda del producto por su ID
            ResultSet rs = conectorBD.hacerBusqueda("Productos", "id", productoId);
            if (rs.next()) {
                int existenciasActuales = rs.getInt("existencias");
                
                if (existenciasActuales >= cantidadIngresada) {
                    int existenciasNuevas = existenciasActuales - cantidadIngresada;
        
                    // Actualizar las existencias del producto en la base de datos
                    String consulta = "UPDATE Productos SET existencias = ? WHERE id = ?";
                    conectorBD.ejecutarConsulta(consulta, existenciasNuevas, productoId);
        
                    // Mostrar mensaje de éxito y vaciar los campos de texto
                    JOptionPane.showMessageDialog(null, "Existencias restadas");
                    conectorBD.vaciarCampos(clave_TextField, cantidad_TextField);
                } else {
                    JOptionPane.showMessageDialog(null, "No hay suficientes existencias");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el producto");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al restar existencias: " + e.toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Cantidad inválida");
        }
        
        
    }//GEN-LAST:event_boton_SacarCantidadActionPerformed

