    private void boton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_BuscarActionPerformed

        try{
            ResultSet rs = conectorBD.hacerBusqueda("Productos", "id", clave_TextField.getText());
            if(rs.next()){
                nombre_TextField.setText(rs.getString("nombre"));
                precio_TextField.setText(rs.getString("precio"));
                proveedor_TextField.setText(rs.getString("proveedor"));
                descripcion_TextArea.setText(rs.getString("descripcion"));
                existencias_TextField.setText(rs.getString("existencias"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al buscar el producto");
        }

    }//GEN-LAST:event_boton_BuscarActionPerformed

    private void boton_AgregarCantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_AgregarCantidadActionPerformed
        // TODO add your handling code here:
        try {
            String productoId = clave_TextField.getText();
            int cantidadIngresada = Integer.parseInt(cantidad_TextField.getText());
        
            // Realizar búsqueda del producto por su ID
            ResultSet rs = conectorBD.hacerBusqueda("Productos", "id", productoId);
            if (rs.next()) {
                int existenciasActuales = rs.getInt("existencias");
                int existenciasNuevas = existenciasActuales + cantidadIngresada;
                // Actualizar las existencias del producto en la base de datos
                String consulta = "UPDATE Productos SET existencias = ? WHERE id = ?";
                conectorBD.ejecutarConsulta(consulta, existenciasNuevas, productoId);
                JOptionPane.showMessageDialog(null, "Existencias agregadas");
                conectorBD.vaciarCampos(clave_TextField, cantidad_TextField);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el producto");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al agregar existencias: " + e.toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Cantidad inválida");
        }
        
        
    }//GEN-LAST:event_boton_AgregarCantidadActionPerformed

